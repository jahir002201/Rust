fn main() {
    let a: i32 = 10;       // Integer
    let b: f64 = 3.14;     // Floating-point number
    let c: bool = true;    // Boolean
    let d: char = 'R';     // Character

    println!("Integer: {}", a);
    println!("Floating-point number: {}", b);
    println!("Boolean: {}", c);
    println!("Character: {}", d);
}
